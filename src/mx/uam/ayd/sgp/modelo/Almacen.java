public class Almacen {
	
	private int cantidad;
	private double descuento;
	
	public int getCantidad() {
		return cantidad;
	}
	
	public double getDescuento() {
		return descuento;
	}
	
	public void setCantidad(int x) {
		cantidad=x;
	}
	
	public void setDescuento(double y) {
		descuento=y;
	}
	
}
